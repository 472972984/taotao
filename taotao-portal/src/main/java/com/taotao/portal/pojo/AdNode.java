package com.taotao.portal.pojo;

/**
 * 用来存储广告位信息的pojo
 * @author 陈汇奇
 *
 */
public class AdNode {

	private int height;
	private int wedth;
	private String src;
	
	private int height2;
	private int wedth2;
	private String src2;
	
	private String alt;
	private String href;
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWedth() {
		return wedth;
	}
	public void setWedth(int wedth) {
		this.wedth = wedth;
	}
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}
	public int getHeight2() {
		return height2;
	}
	public void setHeight2(int height2) {
		this.height2 = height2;
	}
	public int getWedth2() {
		return wedth2;
	}
	public void setWedth2(int wedth2) {
		this.wedth2 = wedth2;
	}
	public String getSrc2() {
		return src2;
	}
	public void setSrc2(String src2) {
		this.src2 = src2;
	}
	public String getAlt() {
		return alt;
	}
	public void setAlt(String alt) {
		this.alt = alt;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	
	
}
